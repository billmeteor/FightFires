package aaa.fightfires.data;

public class AreaData {

  private String id;
  private double firstx;
  private double firsty;
  private double secondx;
  private double secondy;
  private double myx;
  private double myy;
  
  public AreaData() {
    super();
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public double getFirstx() {
    return firstx;
  }

  public void setFirstx(double firstx) {
    this.firstx = firstx;
  }

  public double getFirsty() {
    return firsty;
  }

  public void setFirsty(double firsty) {
    this.firsty = firsty;
  }

  public double getSecondx() {
    return secondx;
  }

  public void setSecondx(double secondx) {
    this.secondx = secondx;
  }

  public double getSecondy() {
    return secondy;
  }

  public void setSecondy(double secondy) {
    this.secondy = secondy;
  }

  public double getMyx() {
    return myx;
  }

  public void setMyx(double myx) {
    this.myx = myx;
  }

  public double getMyy() {
    return myy;
  }

  public void setMyy(double myy) {
    this.myy = myy;
  }
  
}
